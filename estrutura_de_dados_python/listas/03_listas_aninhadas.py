matriz = [
    [1, "a", 2],
    ["b", 3, 4],
    [6, 5, "c"]
]

print(matriz[0]) #assim acesso a coluna zero inteira. A resposta esperada é [1, "a", 2].
print(matriz[0] [0]) #casadinha.assim acesso a coluna zero, elemento zero. A resposta esperada é 1.
print(matriz[0] [-1]) #casadinha.assim acesso a coluna zero, elemento -1. A resposta esperada é 2.
print(matriz[-1] [-1]) #casadinha.assim acesso a coluna -1, elemento -1. A resposta esperada é c.
